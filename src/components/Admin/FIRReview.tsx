  incidentTime: string;
  filedAt: string;
  status: 'DRAFT' | 'PENDING_VERIFICATION' | 'VERIFIED' | 'REGISTERED' | 'UNDER_INVESTIGATION' | 'RESOLVED' | 'CLOSED';
  priority: 'LOW' | 'MEDIUM' | 'HIGH' | 'CRITICAL';
